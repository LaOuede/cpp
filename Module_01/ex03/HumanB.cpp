#include "HumanB.hpp"

/* ************************************************************************** */
/* Constructors and Destructors                                               */
/* ************************************************************************** */
HumanB::HumanB ( string name ) : _m_name( name ), _m_weapon(NULL) { }
HumanB::~HumanB ( ) { }


/* ************************************************************************** */
/* Setters                                                                    */
/* ************************************************************************** */
void HumanB::setWeapon ( Weapon &weapon ) { this->_m_weapon = &weapon; }


/* ************************************************************************** */
/* Functions                                                                  */
/* ************************************************************************** */
void HumanB::attack () {
	if (this->_m_weapon)
		cout << this->_m_name << " get attacks with their " << _m_weapon->getType() << endl;
	else
		cout << this->_m_name << " get attacks with their bare hands" << endl;
}